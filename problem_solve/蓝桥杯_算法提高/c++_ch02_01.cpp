#include <iostream>
using namespace std;
// 编写一个程序，利用强制类型转换打印元音字母大小写10种形式的ASCII码。
// 输出的顺序为：大写的字母A，E，I，O，U的ASCII码，小写的字母a，e，i，o，u的ASCII码。
// 所有的ASCII码都用十进制表示.输出10行,每行一个ASCII码，最后输出一个空行。
int main(void)
{
    cout<<(int)'A'<<endl;
    cout<<(int)'E'<<endl;
    cout<<(int)'I'<<endl;
    cout<<(int)'O'<<endl;
    cout<<(int)'U'<<endl;
    cout<<(int)'a'<<endl;
    cout<<(int)'e'<<endl;
    cout<<(int)'i'<<endl;
    cout<<(int)'o'<<endl;
    cout<<(int)'u'<<endl;
}